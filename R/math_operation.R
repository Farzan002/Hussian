#' Provides mode for a distribution vector
#' @param add Addition.
#' @param sub Subtraction
#' @param mul Multiplication
#' @param div division
#' @return c Respective outputs.



add <- function(a,b){
  c = a+b
  return(c)
}


sub <- function(a,b){
  c = a-b
  return(c)
}


mul <- function(a,b){
  c = a*b
  return(c)
}


div <- function(a,b){
  c = a/b
  return(c)
}

#' Provides mode for a distribution vector
#' @param add1 Addition.
#' @param sub1 Subtraction
#' @param mul1 Multiplication
#' @param div1 division
#' @return c Respective outputs.



add1 <- function(a,b){
  c = a+b
  return(c)
}


sub1 <- function(a,b){
  c = a-b
  return(c)
}


mul1 <- function(a,b){
  c = a*b
  return(c)
}


div1 <- function(a,b){
  c = a/b
  return(c)
}
